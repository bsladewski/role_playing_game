/// <summary>
/// Emotions that can be expressed during dialogue.
/// </summary>
public enum DialogueEmotion
{
    /// <summary>
    /// Does not convey any specific emotion.
    /// </summary>
    NEUTRAL,

    /// <summary>
    /// Conveys anger.
    /// </summary>
    ANGRY,

    /// <summary>
    /// Conveys excitement.
    /// </summary>
    EXCITED,

    /// <summary>
    /// Conveys sadness.
    /// </summary>
    SAD,

    /// <summary>
    /// Conveys fear.
    /// </summary>
    SCARED,
}
