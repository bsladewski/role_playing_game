/// <summary>
/// Emotions that can be expressed during dialogue.
/// </summary>
public enum DialogueEmotion
{
    /// <summary>
    /// Does not convey any specific emotion.
    /// </summary>
    NEUTRAL = 0,

    /// <summary>
    /// Conveys anger.
    /// </summary>
    ANGRY = 1,

    /// <summary>
    /// Conveys excitement.
    /// </summary>
    EXCITED = 2,

    /// <summary>
    /// Conveys sadness.
    /// </summary>
    SAD = 3,

    /// <summary>
    /// Conveys fear.
    /// </summary>
    SCARED = 4,
}
